WITH RandomDTimeIDs AS (
    SELECT
        ec.EnergyConsumptionID,
        dt.DTimeID AS random_DTimeID,
        ROW_NUMBER() OVER (ORDER BY random()) AS rn
    FROM
        EC_Schema.EnergyConsumption AS ec,
        EC_Schema.DTime AS dt
    WHERE
        ec.EnergyConsumptionID IS NOT NULL
)
-- Update DTimeID for each EnergyConsumption with a random DTimeID value
UPDATE EC_Schema.EnergyConsumption AS enc
SET DTimeID = rd.random_DTimeID
FROM RandomDTimeIDs AS rd
WHERE enc.EnergyConsumptionID = rd.EnergyConsumptionID;


-- Create a Common Table Expression (CTE) to generate random values for referencing columns
WITH RandomEnergyUsages AS (
    SELECT
        ec.EnergyConsumptionID,
        eu.EnergyUsageID AS random_EnergyUsageID,
        ROW_NUMBER() OVER (ORDER BY random()) AS rn
    FROM
        EC_Schema.EnergyConsumption AS ec,
        EC_Schema.EnergyUsage AS eu
    WHERE
        ec.EnergyConsumptionID IS NOT NULL
)
-- Update EnergyUsageID for each EnergyConsumption with a random EnergyUsageID value
UPDATE EC_Schema.EnergyConsumption AS enc
SET EnergyUsageID = reu.random_EnergyUsageID
FROM RandomEnergyUsages AS reu
WHERE enc.EnergyConsumptionID = reu.EnergyConsumptionID;

WITH RandomEnergySources AS (
    SELECT
        ec.EnergyConsumptionID,
        es.EnergySourceID AS random_EnergySourceID,
        ROW_NUMBER() OVER (ORDER BY random()) AS rn
    FROM
        EC_Schema.EnergyConsumption AS ec,
        EC_Schema.EnergySource AS es
    WHERE
        ec.EnergyConsumptionID IS NOT NULL
)
-- Update EnergySourceID for each EnergyConsumption with a random EnergySourceID value
UPDATE EC_Schema.EnergyConsumption AS enc
SET EnergySourceID = res.random_EnergySourceID
