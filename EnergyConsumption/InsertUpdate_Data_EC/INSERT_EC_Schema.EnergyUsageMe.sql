WITH RandomEnergyUsages AS (
    SELECT
        e.EnUsageReadingID,
        em.EnergyUsageID AS random_EnergyUsageID,
        ROW_NUMBER() OVER (ORDER BY random()) AS rn
    FROM
        EC_Schema.EnergyUsageMeterReading AS e,
        EC_Schema.EnergyUsage AS em
    WHERE
        e.EnUsageReadingID IS NOT NULL
)
-- Update EnergyUsageID for each EnergyUsageMeterReading with a random EnergyUsageID value
UPDATE EC_Schema.EnergyUsageMeterReading AS c
SET EnergyUsageID = rm.random_EnergyUsageID
FROM RandomEnergyUsages AS rm
WHERE c.EnUsageReadingID = rm.EnUsageReadingID;



WITH RandomEnergyUsages AS (
    SELECT
        e.EnUsageReadingID,
        em.MeterReadingID AS random_MeterReadingID,
        ROW_NUMBER() OVER (ORDER BY random()) AS rn
    FROM
        EC_Schema.EnergyUsageMeterReading AS e,
        EC_Schema.MeterReading AS em
    WHERE
        e.EnUsageReadingID IS NOT NULL
)
-- Update MeterReadingID for each EnergyUsageMeterReading with a random MeterReadingID value
UPDATE EC_Schema.EnergyUsageMeterReading AS c
SET MeterReadingID = rm.random_MeterReadingID
FROM RandomEnergyUsages AS rm
WHERE c.EnUsageReadingID = rm.EnUsageReadingID;


